// ==== API Types & Interfaces ====

export interface AIAskRequest {
  tenantId: string;        // Tenant identifier resolved from session
  userId: string;          // User identifier resolved from session
  currentRoute: string;    // Front-end active route passed on command (e.g. "/products/123")
  question: string;        // Natural language query asked by the merchant
}

export interface AIAskResponse {
  answer: string;          // Natural language response generated by the LLM
  data?: any;              // Structural payloads retrieved by services (optional)
}

// ==== Controller / Handler Mock Implementation ====

export async function handleAIAsk(req: AIAskRequest): Promise<AIAskResponse> {
  // 1. Construct the AI Runtime Context
  const aiContext: AIContext = await buildAIContext({
    tenantId: req.tenantId,
    userId: req.userId,
    currentRoute: req.currentRoute,
  });

  // 2. Decide on Tool Services querying based on intent extraction
  const toolResults: Record<string, any> = {};

  if (isStoreOverviewQuestion(req.question)) {
    toolResults['storeKpis'] = {
      metrics: aiContext.metrics,
    };
  }

  if (
    aiContext.ui.pageType === 'product_detail' &&
    aiContext.ui.productId &&
    isProductQuestion(req.question)
  ) {
    // In actual system, we pull products & orders securely via databases or context props
    // We mock products/orders array for compile safety here
    const productPerformance = ProductService.getProductPerformance(
      aiContext.ui.productId,
      [] // Pass empty or current order items collection
    );
    toolResults['productPerformance'] = productPerformance;
  }

  if (isPaymentQuestion(req.question)) {
    const recommendPaymentsResult = PaymentService.getBestPaymentMethod(
      aiContext.shop.country,
      aiContext.shop.industry
    );
    toolResults['paymentRecommendation'] = recommendPaymentsResult;
  }

  // 3. Assemble compile-stable System Prompt populated with actual contexts
  const systemPrompt = buildSystemPrompt(aiContext, toolResults);

  // 4. In production backend: Call the LLM with systemPrompt and userQuestion
  // Here we return a well-formatted blueprint confirmation indicating successful build
  const mockAnswer = `[AI OS Blueprint Engine Loaded]
Based on the Context-Aware System Prompt, I have processed your request for: "${req.question}".

* Target PageType detected: ${aiContext.ui.pageType}
* Active Industry mapped: ${aiContext.shop.industry}
* Localised Region: ${aiContext.shop.country} (${aiContext.shop.currency})
* Related metrics loaded: ${JSON.stringify(toolResults)}`;

  return {
    answer: mockAnswer,
    data: {
      aiContext,
      toolResults,
      systemPromptText: systemPrompt
    }
  };
}

// ==== Router Intent Mappers ====

function isStoreOverviewQuestion(q: string): boolean {
  const s = q.toLowerCase();
  return (
    s.includes('今天') ||
    s.includes('利润') ||
    s.includes('运营') ||
    s.includes('生意') ||
    s.includes('业绩')
  );
}

function isProductQuestion(q: string): boolean {
  const s = q.toLowerCase();
  return (
    s.includes('这个商品') ||
    s.includes('这款产品') ||
    s.includes('这个产品') ||
    s.includes('商品详情')
  );
}

function isPaymentQuestion(q: string): boolean {
  const s = q.toLowerCase();
  return (
    s.includes('支付') ||
    s.includes('付款') ||
    s.includes('哪个支付') ||
    s.includes('收款')
  );
}

