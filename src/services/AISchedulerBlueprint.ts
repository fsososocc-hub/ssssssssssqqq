/********************************************************
 * Part 6：/api/ai/ask 极简“tools 模式”高级写法示例
 * （给使用 OpenAI / Anthropic function calling 的同事看）
 ********************************************************/

export interface ToolDefinition {
  name: string;
  description: string;
  parameters: any;
  fn: () => Promise<any>;
}

export async function handleAIAskToolsMode(req: AIAskRequest): Promise<AIAskResponse> {
  // 1. 确定本地运行时上下文
  const aiContext = await buildAIContext({
    tenantId: req.tenantId,
    userId: req.userId,
    currentRoute: req.currentRoute,
  });

  // 2. 注册绑定到我们具体业务服务的的可执行函数
  const tools: ToolDefinition[] = [
    {
      name: 'getStoreKpis',
      description: '获取当前店铺的核心运营指标，用于回答“今天怎么样”“利润怎么样”等问题',
      parameters: { type: 'object', properties: {}, required: [] },
      fn: async () => await storeKpiService.getStoreKpis(aiContext.shop.shopId),
    },
    {
      name: 'getProductInsight',
      description: '获取当前商品的运营表现和库存情况，用于回答“这个商品怎么样”',
      parameters: { type: 'object', properties: {}, required: [] },
      fn: async () => {
        if (!aiContext.ui.productId) {
          throw new Error('当前路由上下文中未绑定产品ID属性。');
        }
        return await expandedProductService.getProductInsight(aiContext.ui.productId, aiContext.shop.shopId);
      },
    }
  ];

  // 3. 构建完整的 Master Prompt
  const systemPrompt = buildSystemPrompt(aiContext, {});

  // 4. 将系统提示和工具库签名传输至语言模型
  const answer = await callLLMWithTools({
    systemPrompt,
    userQuestion: req.question,
    tools,
  });

  return { answer };
}

// 实际生产情况中使用的 Mock Tool-callback 大模型调度机制
async function callLLMWithTools(params: {
  systemPrompt: string;
  userQuestion: string;
  tools: ToolDefinition[];
}): Promise<string> {
  // 1. 将 systemPrompt、userQuestion 以及 tools 的 schema 属性发送给 LLM
  // 2. 识别模型是否决定触发 "tool_calls" 的决策列表
  // 3. 本地执行匹配的工具方法：如 await tools[i].fn()
  // 4. 将执行结果 JSON 回传大模型，模型结合最初的 systemPrompt 和工具返回值，输出最终解答文本
  
  return `[Mock Tools Response] LLM 已解析并在本地自动执行了关联工具方法，成功汇总上下文数据并生成决策。`;
}
